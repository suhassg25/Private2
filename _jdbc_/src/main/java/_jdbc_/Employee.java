package _jdbc_;

public class Employee {
	
	private int id;
	private String name;
	private long phone;
	
public void setId(int id)
{
	this.id=id;
}
public int getId()
{
	return id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public long getPhone() {
	return phone;
}
public void setPhone(long phone) {
	this.phone = phone;
}
	
	

}
